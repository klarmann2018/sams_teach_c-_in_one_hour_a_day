#include <iostream>
using namespace std;
int main()
{
    cout << "Enter two Integers: " << endl;
    int Number1, Number2;
    cin >> Number1;
    cin >> Number2;

    cout << "Number1 + Number2= " << Number1 + Number2 << endl;
    cout << "Number1 - Number2= " << Number1 - Number2 << endl;
    cout << "Number1 * Number2= " << Number1 * Number2 << endl;
    cout << "Number1 / Number2= " << Number1 / Number2 << endl;
    cout << "Number1 % Number2= " << Number1 % Number2 << endl;

    return 0;
}
